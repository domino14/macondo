// copyMoveSlice deep-copies the move pointers so a subsequent GenAll
// (which reuses the generator's underlying buffer) can't clobber the
// snapshot we want to compare against.
func copyMoveSlice(in []*move.Move) []*move.Move {
	out := make([]*move.Move, len(in))
	for i, m := range in {
		c := &move.Move{}
		c.CopyFrom(m)
		out[i] = c
	}
	return out
}

func absDiff(a, b float64) float64 {
	if a > b {
		return a - b
	}
	return b - a
}
